import os


# ------------------------------------------------------------------------------
# Offset reader
# ------------------------------------------------------------------------------
def get_offset(path):
    """
    Читает offset из файла.

    Поведение:
    - если файл существует → читаем
    - если файл сломан → возвращаем 0
    - если файла нет → возвращаем 0

    Важно:
    - НИКОГДА не падаем
    - offset = 0 означает "начать с начала"
    """

    try:
        if os.path.exists(path):
            with open(path) as f:
                return int(f.read().strip())
    except Exception:
        # любой мусор → считаем как 0
        return 0

    return 0


# ------------------------------------------------------------------------------
# Offset writer
# ------------------------------------------------------------------------------
def set_offset(path, offset):
    """
    Сохраняет offset в файл.

    Использует атомарную запись:
    1. пишем во временный файл
    2. заменяем основной

    Это защищает от:
    - падения процесса во время записи
    - частично записанных файлов
    """

    tmp_file = path + ".tmp"

    # записываем во временный файл
    with open(tmp_file, "w") as f:
        f.write(str(offset))

    # атомарная замена
    os.replace(tmp_file, path)
